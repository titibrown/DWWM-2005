<?php

/* Opérateurs de comparaison : 

    ==, ===, !=,!==

    */


$number = 14 ; 


if ($number == 14)

{

    echo "number vaut 14";

}

if ($number === 14) #identique 

{

    echo "number vaut 14";

}


if ($number != 14)

{

    echo "number vaut 14";

}

if ($number !== 14) # Fonctionnement sur le Type  = Egalité et même type 

{

    echo "ok";

}